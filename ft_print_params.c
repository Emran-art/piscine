#include <unistd.h>

int	main(int argc, char **argv)
{
	int	j;
	int	k;

	k = 1;
	while (k < argc)
	{
		j = 0;
		while (argv[k][j] != '\0')
		{
			write(1, &argv[k][j], 1);
			j++;
		}
		write(1, "\n", 1);
		k++;
	}
}
